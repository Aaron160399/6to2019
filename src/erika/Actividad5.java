    public Actividad5() {
        initComponents();
        textAutoCompleter = new TextAutoCompleter(jTextField1, new AutoCompleterCallback() {
            @Override
            public void callback(Object o){
                Persona persona = (Persona)textAutoCompleter.findItem(o);
                jTextField2.setText(persona.getEdad()+"");
            }
        });
        loadAutoCompleter();
    }

    void loadAutoCompleter(){
        for (int i = 0; i < 10; i++) {
            Persona persona = new Persona("Name"+i, i);
            textAutoCompleter.addItem(persona);
        }
    }
    
